package Loja;

public class Produto {
    int codigo;
    String nome;
    double preco;
    String cor;
    String tamanho;
    String tipo;

    public Produto(int codigo, String nome, double preco, String cor, String tamanho, String tipo) {
        this.codigo = codigo;
        this.nome = nome;
        this.preco = preco;
        this.cor = cor;
        this.tamanho = tamanho;
        this.tipo = tipo;
    }



}